
//3. Write a program to calculate sum of numbers between 1 and 100

public class Sum_num_zero_hundred {

	public static void main(String[] args) {
		int n = 0;
	    for (int i = 1; i <= 100; i++) {
	    	System.out.println(i);
	        n=n+i;
	    }  
	    System.out.println("The sum of all numbers between 1 and 100 is "+ n +".");
	}

}
